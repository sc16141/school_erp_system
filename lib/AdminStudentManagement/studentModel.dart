class Student {
  final String id;
  final String name;
  final String email;
  final String phone;
  final String className;
  final String admissionStatus;
  final DateTime dateOfBirth;
  final String address;

  Student({
    required this.id,
    required this.name,
    required this.email,
    required this.phone,
    required this.className,
    required this.admissionStatus,
    required this.dateOfBirth,
    required this.address,
  });
}